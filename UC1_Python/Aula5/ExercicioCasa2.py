Numero = int(input("Verifiquei se numero é divisivel por 3 e5., ensira uma numeração: "))
if (Numero%3 == 0):
    print(f"Esse numero é divisivel por 3")
else:
    print("Esse número não divisivel nem por 3.")


if (Numero%5 == 0):
    print(f"Esse numero é divisivel por 5")
else:
    print("Esse número não divisivel nem por 5.")


